
public class Not_booleanvalues {

	public static void main(String[] args) {
            int a =100;
            System.out.println(~a);
            int b = -10;
            System.out.println(~b);
            boolean c = true;
            boolean d = false;
            System.out.println(!c);
            System.out.println(!d);
	}

}
