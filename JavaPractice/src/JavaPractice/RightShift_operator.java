package JavaPractice;
public class RightShift_operator {

	public static void main(String[] args) {
                 //=10/2=5 for one right shift
		         //=5/2= 2.5 for second right shift because right shift value is 2
		         //we get floor value of 2.5 is 2 so answer is 2 and so on for each right shift value
		         System.out.println(10>>2);
		         //20/2 = 10 =0/2 =5 value is 5
                 System.out.println(20>>2);
                 //20/2 = 10, 10/2 = 5, 5/2 =2.5 = 2 for getting floor value
                 System.out.println(20>>3);
	}

}
